#!/usr/bin/env node
/**
 * Persistent local dev infrastructure (Postgres + Redis).
 *
 * Usage:
 *   pnpm dev:services        # start and write packages/cli/bin/.env
 *   pnpm dev:services:down   # stop containers, keep volumes
 *   pnpm dev:services:clean  # stop and delete volumes
 */
import { spawnSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const REPO_ROOT = resolve(import.meta.dirname, '..');
const COMPOSE_FILE = resolve(REPO_ROOT, 'docker-compose.dev.yml');
const ENV_FILE = resolve(REPO_ROOT, 'packages/cli/bin/.env');

const ENV_VARS = {
	DB_TYPE: 'postgresdb',
	DB_POSTGRESDB_HOST: 'localhost',
	DB_POSTGRESDB_PORT: '5433',
	DB_POSTGRESDB_DATABASE: 'n8n_db',
	DB_POSTGRESDB_USER: 'n8n_user',
	DB_POSTGRESDB_PASSWORD: 'test_password',
	EXECUTIONS_MODE: 'queue',
	QUEUE_BULL_REDIS_HOST: 'localhost',
	QUEUE_BULL_REDIS_PORT: '6380',
	N8N_CACHE_ENABLED: 'true',
	N8N_CACHE_BACKEND: 'redis',
	N8N_CACHE_REDIS_HOST: 'localhost',
	N8N_CACHE_REDIS_PORT: '6380',
};

function runCompose(args) {
	return spawnSync('docker', ['compose', '-f', COMPOSE_FILE, ...args], {
		cwd: REPO_ROOT,
		stdio: 'inherit',
	});
}

function writeEnv() {
	const lines = [
		'# Generated by pnpm dev:services — persistent Postgres + Redis for local dev',
		'# Stop (keep data): pnpm dev:services:down',
		'# Reset data:       pnpm dev:services:clean',
		'',
		...Object.entries(ENV_VARS).map(([key, value]) => `${key}=${value}`),
		'',
	];
	writeFileSync(ENV_FILE, lines.join('\n'));
}

const command = process.argv[2] ?? 'up';

switch (command) {
	case 'up': {
		const result = runCompose(['up', '-d', '--wait']);
		if (result.status !== 0) {
			process.exit(result.status ?? 1);
		}
		writeEnv();
		console.log('');
		console.log('Dev services ready (data persists across restarts).');
		console.log('  Postgres: localhost:5433');
		console.log('  Redis:    localhost:6380');
		console.log(`  Env file: packages/cli/bin/.env`);
		console.log('');
		console.log('Next: cd packages/cli && pnpm dev');
		break;
	}
	case 'down': {
		const result = runCompose(['down']);
		process.exit(result.status ?? 1);
		break;
	}
	case 'clean': {
		const result = runCompose(['down', '-v']);
		process.exit(result.status ?? 1);
		break;
	}
	default: {
		console.error('Usage: node scripts/dev-services.mjs [up|down|clean]');
		process.exit(2);
	}
}
